#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	len;

	len = ft_strlen(s1) + 1;
	str = malloc(sizeof(char) * len);
	if (!str || !s1)
		return (0);
	str = ft_memcpy(str, s1, len);
	return (str);
}